package io.webfolder.cormorant.api.model;

public class Domain {

    private final String name;

    private final String id;

    public Domain(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    @Override
    public String toString() {
        return "Domain [name=" + name + ", id=" + id + "]";
    }
}
